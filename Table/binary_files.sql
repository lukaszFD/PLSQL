CREATE TABLE BINARY_FILES (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FILE_NAME     VARCHAR2(255),
    MIME_TYPE     VARCHAR2(100),
    UPLOAD_DATE   DATE NOT NULL,
    FILE_DATA     BLOB
)
PARTITION BY RANGE (UPLOAD_DATE) INTERVAL (NUMTOYMINTERVAL(1, 'MONTH'))
(
    PARTITION p_start VALUES LESS THAN (TO_DATE('2024-01-01', 'YYYY-MM-DD')) TABLESPACE FILES_TS
)
LOB (FILE_DATA) STORE AS SECUREFILE (
    TABLESPACE FILES_TS
    ENABLE STORAGE IN ROW
    CHUNK 8192
    CACHE
    NOCOMPRESS
    KEEP_DUPLICATES
);